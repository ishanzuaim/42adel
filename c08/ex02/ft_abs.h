#ifndef FT_ABS_H
# define FT_ABS_H
# define ABS(Value) \
({\
 	long answer; \
	 if(Value < 0) { \
		answer = -Value; \
	} \
	else { \
		answer = Value; \
	} \
	answer ; \
}) 
#endif
