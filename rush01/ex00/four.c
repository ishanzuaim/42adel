int get_index(int x, int y, int* size)
{
	int keys[9][3] = {
		{ 4, 1, 1 }, { 3, 2, 3 }, { 2, 2, 6 }, { 3, 1, 3 }, { 2, 1, 2 },
		{ 1, 4, 1 }, { 2, 3, 3 }, { 1, 3, 3 }, { 1, 2, 2 }
	};

	int i = 0;
	while (i < 5)
	{
		if(keys[i][0] == x && keys[i][1] == y) {
			*size = keys[i][2];
			return i;
		}
		i++;
	}
	return 0;
}

